char	*ft_strchr(const char *s, int c_int)
{
	char	c;

	c = (char)c_int;
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (0);
}
